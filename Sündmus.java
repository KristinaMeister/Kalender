
public class Sündmus {
    private int kuupäev;
    private int kuu;
    private int aasta;
    private String kirjeldus;

    public Sündmus(int kuupäev, int kuu, int aasta, String kirjeldus) {
        this.kuupäev = kuupäev;
        this.kuu = kuu;
        this.aasta = aasta;
        this.kirjeldus = kirjeldus;
    }
}
